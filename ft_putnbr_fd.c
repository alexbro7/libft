#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	x;

	x = 1;
	if (n == -2147483648)
	{
		ft_putstr_fd("-2147483648", fd);
		return ;
	}
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n *= -1;
	}
	while (n / x > 9)
		x *= 10;
	while (x)
	{
		ft_putchar_fd((n / x) + 48, fd);
		n %= x;
		x /= 10;
	}
}
